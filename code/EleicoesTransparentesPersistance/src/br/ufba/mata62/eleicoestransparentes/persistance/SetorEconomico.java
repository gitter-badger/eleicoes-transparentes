package br.ufba.mata62.eleicoestransparentes.persistance;

public class SetorEconomico {
	
	private int id;

	private String codSetorEco;

	private String nome;

	public String getCodSetorEco() {
		return codSetorEco;
	}

	public void setCodSetorEco(String codSetorEco) {
		this.codSetorEco = codSetorEco;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	
	
}
